//! Demonstrates loading a model that contains an STL mesh file.
//!
//! Starting with MuJoCo 3.6.0, STL and OBJ mesh decoding is handled by
//! plugin shared libraries rather than the MuJoCo core. This example shows
//! how to:
//!
//! 1. Call [`load_all_plugin_libraries`] to load all plugins from MuJoCo's
//!    plugin directory so that the STL decoder is available.
//! 2. Inject an in-memory STL asset and a matching MJCF model into a
//!    [`MjVfs`] virtual file system.
//! 3. Load the model via [`MjModel::from_xml_vfs`].
//! 4. Inspect the loaded mesh count.
//!
//! # Usage
//!
//! ```text
//! cargo run --example stl_mesh -- /path/to/mujoco/bin/mujoco_plugin
//! ```
//!
//! The argument must point to the directory that contains the decoder plugin
//! shared libraries (e.g. `libstl_decoder.so` on Linux).

use mujoco_rs::prelude::*;
use mujoco_rs::wrappers::mj_auxiliary::MjVfs;

// ---------------------------------------------------------------------------
// Embedded STL -- a minimal tetrahedron
// ---------------------------------------------------------------------------
// Binary STL format:
//   80-byte header (unused) | u32 triangle count | N * 50-byte triangle records
//   Each triangle: 3x f32 normal | 3x (3x f32) vertex | u16 attribute byte count
//
// Vertices: V0=(0,0,0)  V1=(1,0,0)  V2=(0,1,0)  V3=(0,0,1)
const TETRA_STL: &[u8] = &[
    // 80-byte header (all zeros)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // triangle count: 4
    0x04, 0x00, 0x00, 0x00,
    // face 0: normal (0,0,-1) -- V0 V2 V1
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0xbf,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00,
    // face 1: normal (0,-1,0) -- V0 V1 V3
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0xbf, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f,
    0x00, 0x00,
    // face 2: normal (-1,0,0) -- V0 V3 V2
    0x00, 0x00, 0x80, 0xbf, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00,
    // face 3: normal (1/sqrt(3), 1/sqrt(3), 1/sqrt(3)) -- V1 V2 V3
    0x3a, 0xcd, 0x13, 0x3f, 0x3a, 0xcd, 0x13, 0x3f, 0x3a, 0xcd, 0x13, 0x3f,
    0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x3f,
    0x00, 0x00,
];

const MODEL_XML: &str = r#"<mujoco>
  <asset>
    <mesh name="tetra" file="tetra.stl"/>
  </asset>
  <worldbody>
    <geom type="mesh" mesh="tetra"/>
  </worldbody>
</mujoco>"#;

fn main() {
    // Obtain the plugin directory from the first command-line argument.
    let plugin_dir = match std::env::args().nth(1) {
        Some(dir) => dir,
        None => {
            eprintln!(
                "Usage: stl_mesh <plugin-dir>\n\n\
                 Example: cargo run --example stl_mesh -- \
                 /path/to/mujoco/bin/mujoco_plugin\n\n\
                 The directory must contain the decoder plugin shared libraries \
                 (e.g. libstl_decoder.so on Linux)."
            );
            std::process::exit(1);
        }
    };

    // Load all MuJoCo plugins from the plugin directory.
    // This registers the STL (and OBJ) decoder plugins required by MuJoCo 3.6.0+.
    println!("Loading plugins from: {plugin_dir}");
    load_all_plugin_libraries(&plugin_dir, None)
        .expect("failed to load plugin libraries");
    println!("Plugins loaded successfully.");

    // Inject the in-memory STL asset and the MJCF model into a virtual file system
    // so that no files need to exist on disk.
    let mut vfs = MjVfs::new();
    vfs.add_from_buffer("tetra.stl", TETRA_STL)
        .expect("failed to add STL to VFS");
    vfs.add_from_buffer("model.xml", MODEL_XML.as_bytes())
        .expect("failed to add model XML to VFS");

    // Load the model -- the STL decoder plugin is called transparently.
    let model = MjModel::from_xml_vfs("model.xml", &vfs)
        .expect("failed to load model with STL mesh");

    println!(
        "Model loaded successfully: {} mesh(es), {} geom(s).",
        model.nmesh(),
        model.ngeom()
    );
}
